function createOGSVG(width: number, height: number): string {
  const logoSize = 180;
  const logoX = (width - logoSize) / 2;
  const logoY = (height - logoSize) / 2 - 40;

  return `<svg width="${width}" height="${height}" viewBox="0 0 ${width} ${height}" xmlns="http://www.w3.org/2000/svg">
  <rect width="${width}" height="${height}" fill="${BRAND.dark}"/>
  <g transform="translate(${logoX}, ${logoY}) scale(${logoSize / 257})">
    <path d="${LOGO_PATH}" fill="${BRAND.yellow}"/>
  </g>
  <text x="${width / 2}" y="${logoY + logoSize + 60}"
        font-family="system-ui, -apple-system, sans-serif"
        font-size="48"
        font-weight="900"
        fill="${BRAND.white}"
        text-anchor="middle"
        letter-spacing="-0.02em">
    CRAFTER STATION
  </text>
  <text x="${width / 2}" y="${logoY + logoSize + 100}"
        font-family="system-ui, -apple-system, sans-serif"
        font-size="24"
        fill="#A3A3A3"
        text-anchor="middle">
    Team Organization
  </text>
</svg>`;
}

function createFaviconSVG(size: number): string {
  return `<svg width="${size}" height="${size}" viewBox="0 0 257 257" xmlns="http://www.w3.org/2000/svg">
  <rect width="257" height="257" fill="${BRAND.dark}"/>
  <path d="${LOGO_PATH}" fill="${BRAND.yellow}"/>
</svg>`;
}

async function generateOGImage(width: number, height: number, filename: string) {
  const svg = createOGSVG(width, height);
  const pngFilename = filename.replace(".svg", ".png");

  writeFileSync(join(publicDir, filename), svg);

  await sharp(Buffer.from(svg)).png().toFile(join(publicDir, pngFilename));
  console.log(`Generated ${pngFilename}`);
}

async function generateFavicon() {
  const svg = createFaviconSVG(32);
  writeFileSync(join(publicDir, "favicon.svg"), svg);

  await sharp(Buffer.from(createFaviconSVG(32)))
    .png()
    .toFile(join(publicDir, "favicon-32.png"));

  await sharp(Buffer.from(createFaviconSVG(16)))
    .resize(16, 16)
    .png()
    .toFile(join(publicDir, "favicon-16.png"));

  console.log("Generated favicon.svg, favicon-16.png, favicon-32.png");
}

async function generateAppleTouchIcon() {
  const size = 180;
  const padding = 30;
  const logoSize = size - padding * 2;

  const svg = `<svg width="${size}" height="${size}" viewBox="0 0 ${size} ${size}" xmlns="http://www.w3.org/2000/svg">
  <rect width="${size}" height="${size}" fill="${BRAND.dark}"/>
  <g transform="translate(${padding}, ${padding}) scale(${logoSize / 257})">
    <path d="${LOGO_PATH}" fill="${BRAND.yellow}"/>
  </g>
</svg>`;

  writeFileSync(join(publicDir, "apple-touch-icon.svg"), svg);

  await sharp(Buffer.from(svg)).png().toFile(join(publicDir, "apple-touch-icon.png"));
  console.log("Generated apple-touch-icon.png");
}

async function main() {
  if (!existsSync(publicDir)) {
    mkdirSync(publicDir, { recursive: true });
  }

  await generateOGImage(1200, 630, "og.svg");
  await generateOGImage(1200, 600, "og-twitter.svg");
  await generateFavicon();
  await generateAppleTouchIcon();

  console.log("\nBrand assets generated successfully!");
}

